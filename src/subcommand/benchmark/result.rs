//! Benchmark result serialization utilities
//!
//! This module provides a standardized serializable wrapper around types
//! that represent the outcome of a benchmark execution, ensuring uniform
//! formatting when persisting results to disk

use serde::Serialize;

/// Represents the final outcome of a benchmark execution to be written into
/// the benchmark result files
///
/// This enum normalizes both successful runs and failures into a structured,
/// serializable format, using lowercase tags for compatibility with data
/// formats like JSON
#[derive(Debug, Serialize)]
#[serde(rename_all = "lowercase")]
pub enum Result<T>
where
  T: Serialize,
{
  /// Contains the error message if the benchmark failed to execute
  Error(String),
  /// Contains the successful output data generated by the benchmark
  Ok(T),
}

impl<T> From<anyhow::Result<T>> for Result<T>
where
  T: Serialize,
{
  fn from(val: anyhow::Result<T>) -> Self {
    match val {
      Err(e) => Self::Error(format!("{e:#}")),
      Ok(r) => Self::Ok(r),
    }
  }
}
